#include "stdio.h"
int main()
{
    for (int i = 123; i < 333; i++)
    {
        int a[10] = { 0 }, b[10] = { 0 }, c[10] = { 0 };
        a[1] = i % 100 % 10;//个位
        a[2] = i % 100 / 10;//十位
        a[3] = i / 100;//百位
        b[1] = 2 * i % 10 % 10;//个位
        b[2] = 2 * i % 100 / 10;//十位
        b[3] = 2 * i / 100;//百位
        c[1] = 3 * i % 10 % 10;//个位
        c[2] = 3 * i % 100 / 10;//十位
        c[3] = 3 * i / 100;//百位
        if (a[1] * a[2] * a[3] * b[1] * b[2] * b[3] * c[1] * c[2] * c[3] == 362880 && a[1] + a[2] + a[3] + b[1] + b[2] + b[3] + c[1] + c[2] + c[3] == 45)
        {
            printf("%d %d %d\n", i, 2 * i, 3 * i);
        }
    }
    return 0;
}
